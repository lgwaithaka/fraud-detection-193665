@app.post("/predict", response_model=FraudResponse, tags=["Inference"])
async def predict(claim: ClaimRequest):
    """
    Score a single motor insurance claim.

    Returns fraud probability (0–1), risk decision, top-5 SHAP factors,
    plain-English adjuster summary, and recommended action.
    """
    if not ml:
        raise HTTPException(
            status_code=503,
            detail="Model not loaded. Run DSA8502_FraudDetection_193665.ipynb first."
        )

    # 1. Feature engineering
    data = claim.model_dump()
    data = _engineer(data)

    # 2. Preprocess
    X_sc = _preprocess(data)

    # 3. Predict
    thresh = ml["config"].get("business_threshold", 0.40)
    prob   = float(ml["model"].predict_proba(X_sc)[0, 1])

    # 4. Decision tier
    if prob >= thresh:
        decision   = "FRAUD ALERT"
        risk_level = "CRITICAL" if prob >= 0.75 else "HIGH"
    elif prob >= thresh * 0.6:
        decision   = "NEEDS REVIEW"
        risk_level = "MEDIUM"
    else:
        decision   = "LIKELY LEGITIMATE"
        risk_level = "LOW"

    # 5. SHAP top-5
    try:
        sv      = ml["explnr"].shap_values(X_sc)[0]
        top_idx = np.argsort(np.abs(sv))[::-1][:5]
        factors = [
            SHAPFactor(
                feature   = ml["features"][i],
                shap_value= round(float(sv[i]), 4),
                direction = "increases_fraud_risk" if sv[i] > 0 else "decreases_fraud_risk",
            )
            for i in top_idx
        ]
    except Exception:
        factors = []

    # 6. Summary
    summary, action = _summary(data, prob, factors)

    return FraudResponse(
        claim_id           = f"CLM-{uuid.uuid4().hex[:8].upper()}",
        fraud_probability  = round(prob, 4),
        fraud_probability_pct = f"{prob*100:.1f}%",
        decision           = decision,
        risk_level         = risk_level,
        threshold          = thresh,
        top_shap_factors   = factors,
        adjuster_summary   = summary,
        recommended_action = action,
    )


