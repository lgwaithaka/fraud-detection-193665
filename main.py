# Response schema
# ─────────────────────────────────────────────────────────────────────────────
class SHAPFactor(BaseModel):
    feature: str
    shap_value: float
    direction: str  # "increases_fraud_risk" | "decreases_fraud_risk"


class FraudResponse(BaseModel):
    claim_id: str
    fraud_probability: float
    fraud_probability_pct: str
    decision: str        # FRAUD ALERT | NEEDS REVIEW | LIKELY LEGITIMATE
    risk_level: str      # CRITICAL | HIGH | MEDIUM | LOW
    threshold: float
    top_shap_factors: list[SHAPFactor]
    adjuster_summary: str
    recommended_action: str


# ─────────────────────────────────────────────────────────────────────────────
# Helpers
# ─────────────────────────────────────────────────────────────────────────────
def _engineer(data: dict) -> dict:
    """Compute the 8 engineered features from raw inputs."""
    total = data["InjuryClaim"] + data["PropertyClaim"] + data["VehicleClaim"]
    data["TotalClaim"]                = total
    data["early_claim_flag"]          = 1 if data["days_into_policy"] < 30 else 0
    data["rapid_notify_flag"]         = 1 if data["days_loss_to_notify"] <= 1 else 0
    data["multi_claim_flag"]          = 1 if data["claims_same_policy_num"] >= 3 else 0
    data["external_channel_flag"]     = 1 if data["channel_type"] in {"BROKER", "AGENT", "BANCASSURANCE"} else 0
    data["claim_to_deductible_ratio"] = min(total / max(data["Deductible"], 1), 1000.0)
    data["injury_claim_fraction"]     = data["InjuryClaim"] / max(total, 1.0)
    data["expaq_confirmed_flag"]      = 1 if data.get("data_source") == "EXPAQ_DECLINED" else 0
    return data


def _preprocess(data: dict) -> np.ndarray:
    """Encode categoricals, align feature order, apply scaler."""
    row = {}
    for feat in ml["features"]:
        val = data.get(feat, 0)
        enc = ml["encoders"].get(feat)
        if enc is not None:
            sv = str(val)
            val = int(enc.transform([sv])[0]) if sv in enc.classes_ else 0
        row[feat] = val
    X = np.array([[row[f] for f in ml["features"]]], dtype=np.float32)
    return ml["scaler"].transform(X)


def _summary(data: dict, prob: float, factors: list[SHAPFactor]) -> tuple[str, str]:
    """Build plain-English adjuster summary and recommended action."""
    reasons = []
    if data.get("early_claim_flag"):
        reasons.append(
            f"Claim filed {data['days_into_policy']} day(s) into policy "
            f"(IRA Kenya primary red flag — 3.8× higher fraud odds)"
        )
    if data.get("multi_claim_flag"):
        reasons.append(
            f"{data['claims_same_policy_num']} claims on this policy — "
            f"fraud ring pattern (5.2× higher fraud odds)"
        )
    if data.get("rapid_notify_flag"):
        reasons.append(
            f"Loss notified {data['days_loss_to_notify']} day(s) after event — "
            f"pre-arranged incident pattern (2.9× higher fraud odds)"
        )
    if data.get("external_channel_flag"):
        reasons.append(
            f"Externally sourced via {data['channel_type']} — "
            f"1.7× higher base fraud rate"
        )
    ratio = data.get("claim_to_deductible_ratio", 0)
    if ratio > 10:
        reasons.append(f"Total claim is {ratio:.0f}× the deductible — potential inflation")
    if data.get("injury_claim_fraction", 0) > 0.5 and data["InjuryClaim"] > 0:
        reasons.append(
            f"Injury component is {data['injury_claim_fraction']*100:.0f}% of total — "
            f"exaggerated medical claim pattern (avg fraud: 61%)"
        )

    pct = prob * 100
    if reasons:
        bullet = "; ".join(f"({i+1}) {r}" for i, r in enumerate(reasons))
        summary = (
            f"Fraud probability: {pct:.1f}%. "
            f"Risk factors: {bullet}. "
            f"Top SHAP driver: {factors[0].feature} ({factors[0].shap_value:+.3f})."
            if factors else f"Fraud probability: {pct:.1f}%. Risk factors: {bullet}."
        )
    else:
        summary = (
            f"Fraud probability: {pct:.1f}%. "
            "No major individual risk flags detected. Low-risk profile."
        )

    thresh = ml["config"].get("business_threshold", 0.40)
    if prob >= thresh:
        action = (
            "REFER TO SIU — Do not pay without investigation. "
            "Request full documentation, independent vehicle inspection, "
            "and cross-reference against fraud register."
        )
    elif prob >= thresh * 0.6:
        action = (
            "ENHANCED REVIEW — Assign to senior adjuster. "
            "Verify police report, repair estimates, and witness statements."
        )
    else:
        action = "STANDARD PROCESSING — No major fraud indicators. Proceed via normal workflow."

    return summary, action


